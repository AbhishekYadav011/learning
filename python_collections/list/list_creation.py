# creating a list
List = []
print(List)
# creating a list of numbers
List = [4,2,6,10]
print(List)

# creating a list of strings
List = ["abhi","tinku","nepali","tedu"]
print(List)
print(List[3])
print(List[2])

# creating multidimensional list
List = [["abhi","jerry","tom"],[1,45,78]]
print(List[0][0])

# creating list with multiple distinct or duplicate
L1 = [1,2,3,4,56,2,4,6,3]
print(L1)

L2 = [1,3,5,"abhi","tinku"]
print(L2)

# knowing the size of list
print("Size of L1 list is :", len(L1))
print("Size of L2 list is:", len(L2))